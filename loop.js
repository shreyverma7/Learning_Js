console.log("-----------22----FOR LOOP------------------");
for (let j = 0; j < 5; j++) { // Initialization, Condition, Update
    console.log("For Loop iteration: " + j);
}   

console.log("-----------23----DO WHILE LOOP------------------");
let k = 0; // Initialize
do {
    console.log("Do While Loop iteration: " + k);
    k++; // Update
} while (k < 5); // Condition check     